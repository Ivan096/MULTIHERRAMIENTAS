    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
        // TODO add your handling code here:
        String SQL="";
        int fila=JLista.getSelectedRow();
        try{
               ConexionDB con = new ConexionDB();
               Connection cnn = con.MySQLConnect();    
               Statement st = cnn.createStatement();
               SQL="Update PRODUCTOS set NOMBRE_PRODUCTO=?,NUMERO_PIEZAS=?, PRECIO_PRODUCTO=?, STOCK_MINIMO=?, FECHA_MODIFICACION=?, HORA_MODIFICACION=?"+"where ID_PRODUCTO=?";
               String dao =(String)JLista.getValueAt(fila, 0);
               PreparedStatement ps = cnn.prepareCall(SQL);
               ps.setString(1, txtproducto.getText());
               ps.setInt(2, Integer.parseInt(Spiezas.getValue().toString()));
               ps.setDouble(3, Double.parseDouble(txtprecio.getText()));
               ps.setInt(4, Integer.parseInt(Sstock.getValue().toString()));
               ps.setDate(5, fechaActual);
               ps.setString(6, hora);
               ps.setString(7, dao);
               int n= ps.executeUpdate();
               if(n>0){
                    JOptionPane.showMessageDialog(null, "Datos guardados correctamente");
                }

            }catch (Exception e){
                        JOptionPane.showMessageDialog(null, "Error"+e.getMessage());            
            }
        
        Llenar();
        Limpiar();
    }//GEN-LAST:event_BtnModificarActionPerformed

