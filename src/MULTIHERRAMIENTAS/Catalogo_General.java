    public void Llenar(){
        String SQL="";
        try {
            ConexionDB con = new ConexionDB();
            Connection cnn = con.MySQLConnect();
            SQL="select * from PRODUCTOS";
            String[]titulos = {"ID_PRODUCTO","NOMBRE_PRODUCTO","NUMERO_PIEZAS","PRECIO_PRODUCTO","STOCK_MINIMO"};
            model = new DefaultTableModel(null, titulos);
            Statement st = cnn.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            
            String[]fila=new String[5];
            while(rs.next()){
                fila[0] = rs.getString("ID_PRODUCTO");
                fila[1] = rs.getString("NOMBRE_PRODUCTO");
                fila[2] = rs.getString("NUMERO_PIEZAS");
                fila[3] = rs.getString("PRECIO_PRODUCTO");
                fila[4] = rs.getString("STOCK_MINIMO");
                
                model.addRow(fila);
            }
            JLista.setModel(model);
        } catch (Exception e){
    
            e.printStackTrace();
        }
       
        
            
        
        
        
    }
    
