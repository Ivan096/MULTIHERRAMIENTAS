    private void JListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JListaMouseClicked
        // TODO add your handling code here:
        String SQL="";
        if(evt.getButton()==1){
            int fila=JLista.getSelectedRow();
            try{
               ConexionDB con = new ConexionDB();
               Connection cnn = con.MySQLConnect();    
               Statement st = cnn.createStatement();
               Habilitar();
               SQL="select * from PRODUCTOS where ID_PRODUCTO="+JLista.getValueAt(fila, 0);
               ResultSet rs = st.executeQuery(SQL);
               rs.next();
               txtproducto.setText(rs.getString("NOMBRE_PRODUCTO"));
               txtprecio.setText(rs.getString("PRECIO_PRODUCTO"));
               Sstock.setValue(rs.getInt("STOCK_MINIMO"));
               Spiezas.setValue(rs.getInt("NUMERO_PIEZAS"));

            }catch (Exception ex){
            ex.printStackTrace();
            
            }
        }
    }//GEN-LAST:event_JListaMouseClicked

